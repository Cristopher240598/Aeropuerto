    private void modificarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_modificarActionPerformed
    {//GEN-HEADEREND:event_modificarActionPerformed
        NodoArbol d[] = new NodoArbol[2];
        try
        {
            sCom[0] = (String) elementosNivel1.getSelectedItem();
            sCom[1] = (String) elementosNivel2.getSelectedItem();
            sCom[2] = (String) elementosNivel3.getSelectedItem();
            arrCom = ManipulaMultista.elimina((String) elementosNivel3.getSelectedItem(), VentanaPrincipal.r, 0, sCom);
            VentanaPrincipal.r = arrCom[1];
            VentanaPrincipal.aR3.elimina(VentanaPrincipal.aR3.getR(), (String) elementosNivel3.getSelectedItem(), d);
            VentanaPrincipal.aR3.setR(d[1]);
            new VentanaModCompania().setVisible(true);
        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, "Error", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_modificarActionPerformed

