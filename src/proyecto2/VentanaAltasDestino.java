    private void aceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_aceptarActionPerformed
    {//GEN-HEADEREND:event_aceptarActionPerformed
        try
        {
            String d = destino.getText().trim();
            if (d.length() == 0 || ftDestino.getIcon() == null)
            {
                JOptionPane.showMessageDialog(this, "Hay datos sin llenar", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
            } else
            {
                Destino dst = new Destino(destino.getText(), ftDestino.getIcon());
                Nodo nd = new Nodo(dst, destino.getText());
                String s[] = new String[2];
                s[0] = (String) elementosNivel1.getSelectedItem();
                s[1] = destino.getText();
                VentanaPrincipal.r = ManipulaMultista.inserta(VentanaPrincipal.r, nd, s, 0);
                NodoArbol ndA = new NodoArbol(destino.getText());
                ndA.setHh(nd);
                VentanaPrincipal.aR2.setR(VentanaPrincipal.aR2.inserta(VentanaPrincipal.aR2.getR(), ndA));
                Mensaje.exito(this, "Dato insertado correctamente");
                cancelarActionPerformed(null);
            }
        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, "Error", "Mensaje de Error", JOptionPane.ERROR_MESSAGE);
            cancelarActionPerformed(null);
        }
    }//GEN-LAST:event_aceptarActionPerformed

